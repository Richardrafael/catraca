/* DOCUMENTA��O DE TRIGGER DO PROJETO */
/*         Trigger de Catraca         */
CREATE OR REPLACE TRIGGER port_catraca.TRG_LOG_CATRACAS
BEFORE UPDATE OR INSERT OR DELETE
ON port_catraca.CATRACA
REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
BEGIN
   IF Inserting THEN

     --INSERT
     INSERT INTO port_catraca.LOG_CATRACAS
     SELECT SEQ_LOG_CATRACAS.NEXTVAL,
     --DADOS ALTERADOS
     :new.CD_CATRACA,
     :new.NM_CATRACA,
     :new.DS_CATRACA,
     :new.ID_CATRACA,
     :new.SN_ATIVO,
     --CAMPOS PADRAO
     :new.CD_USUARIO_CADASTRO,
     :new.HR_CADASTRO,
     :new.CD_USUARIO_ULT_ALT,
     :new.HR_ULT_ALT,
     --INFORMACOES LOG
     'I' AS TP_ALTERACAO, NULL TP_UPDATE, SYSTIMESTAMP AS HR_LOG, USER AS CD_USUARIO_DB_LOG,

     :new.IP_CATRACA AS IP_CATRACA
     FROM DUAL;
     
   ELSIF Updating THEN
     
     --UPDATE (ANTES)
     INSERT INTO port_catraca.LOG_CATRACAS
     SELECT SEQ_LOG_CATRACAS.NEXTVAL,
     --DADOS ALTERADOS
     :old.CD_CATRACA,
     :old.NM_CATRACA,
     :old.DS_CATRACA,
     :old.ID_CATRACA,
     :old.SN_ATIVO,
     --CAMPOS PADRAO
     :old.CD_USUARIO_CADASTRO,
     :old.HR_CADASTRO,
     :old.CD_USUARIO_ULT_ALT,
     :old.HR_ULT_ALT,
     --INFORMACOES LOG
     'U' AS TP_ALTERACAO, 'A' TP_UPDATE, SYSTIMESTAMP AS HR_LOG, USER AS CD_USUARIO_DB_LOG,

     :old.IP_CATRACA AS IP_CATRACA
     FROM DUAL;

     --UPDATE (DEPOIS)
     INSERT INTO port_catraca.LOG_CATRACAS
     SELECT SEQ_LOG_CATRACAS.NEXTVAL,
     --DADOS ALTERADOS
     :new.CD_CATRACA,
     :new.NM_CATRACA,
     :new.DS_CATRACA,
     :new.ID_CATRACA,
     :new.SN_ATIVO,
     --CAMPOS PADRAO
     :new.CD_USUARIO_CADASTRO,
     :new.HR_CADASTRO,
     :new.CD_USUARIO_ULT_ALT,
     :new.HR_ULT_ALT,
     --INFORMACOES LOG
     'U' AS TP_ALTERACAO, 'D' TP_UPDATE, SYSTIMESTAMP AS HR_LOG, USER AS CD_USUARIO_DB_LOG,

     :new.IP_CATRACA AS IP_CATRACA
     FROM DUAL;
     
   ELSIF Deleting THEN
     --DELETE
     INSERT INTO port_catraca.LOG_CATRACAS
     SELECT SEQ_LOG_CATRACAS.NEXTVAL,
     --DADOS ALTERADOS
     :old.CD_CATRACA,
     :old.NM_CATRACA,
     :old.DS_CATRACA,
     :old.ID_CATRACA,
     :old.SN_ATIVO,
     --CAMPOS PADRAO
     :old.CD_USUARIO_CADASTRO,
     :old.HR_CADASTRO,
     :old.CD_USUARIO_ULT_ALT,
     :old.HR_ULT_ALT,
     --INFORMACOES LOG
     'D' AS TP_ALTERACAO, NULL TP_UPDATE, SYSTIMESTAMP AS HR_LOG, USER AS CD_USUARIO_DB_LOG,

     :old.IP_CATRACA AS IP_CATRACA
     FROM DUAL;
   END IF;
END;
